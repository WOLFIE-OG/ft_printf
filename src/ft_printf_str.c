#include "../include/ft_printf.h"

size_t	ft_printf_str(char *s, int fd)
{
	size_t	i;

	if (!s)
		s = "(null)";
	i = 0;
	while (*s)
		i += ft_printf_char(*s++, fd);
	return (i);
}
